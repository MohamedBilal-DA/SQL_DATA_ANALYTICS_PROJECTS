--1.SCHEMA OF NETFLIX_DATABASE

DROP TABLE IF EXISTS NETFLIX_DATA;

CREATE TABLE NETFLIX_DATA(
SHOW_ID VARCHAR(6),
TYPEE VARCHAR(7),
TITLE VARCHAR(104),
DIRECTOR VARCHAR(209),
CASTT VARCHAR(1000),
COUNTRY VARCHAR(123),
DATE_ADDED VARCHAR(50),
RELEASING_YEAR INTEGER,
RATING VARCHAR(10),
DURATION VARCHAR(11),
LISTED_IN VARCHAR(100),
DESCRIPTION VARCHAR(250)
);

SELECT *
FROM NETFLIX_DATA;
