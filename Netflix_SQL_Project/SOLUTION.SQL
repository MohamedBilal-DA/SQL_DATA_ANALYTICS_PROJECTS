--NETFLIX PROJECT
DROP TABLE IF EXISTS NETFLIX_DATA;

CREATE TABLE NETFLIX_DATA(
SHOW_ID VARCHAR(6),
TYPEE VARCHAR(7),
TITLE VARCHAR(104),
DIRECTOR VARCHAR(209),
CASTT VARCHAR(1000),
COUNTRY VARCHAR(123),
DATE_ADDED VARCHAR(50),
RELEASING_YEAR INTEGER,
RATING VARCHAR(10),
DURATION VARCHAR(11),
LISTED_IN VARCHAR(100),
DESCRIPTION VARCHAR(250)
);

SELECT *
FROM NETFLIX_DATA;

-- 15 Business Problems & Solutions

--1. Count the number of Movies vs TV Shows

SELECT COUNT(*) AS TOTAL_CONTENT,TYPEE
FROM NETFLIX_DATA
GROUP BY TYPEE;

--2. Find the most common rating for movies and TV shows

SELECT TYPEE,RATING,CNT
FROM
(SELECT TYPEE,RATING,COUNT(*) AS CNT,DENSE_RANK() OVER(PARTITION BY TYPEE ORDER BY COUNT(*) DESC) AS DR
FROM NETFLIX_DATA
GROUP BY TYPEE,RATING)RATING
WHERE RATING.DR=1;

--3. List all movies released in a specific year (e.g., 2020)

SELECT TITLE,RELEASING_YEAR
FROM NETFLIX_DATA
WHERE RELEASING_YEAR IN (2020) AND TYPEE IN('Movie');


--4. Find the top 5 countries with the most content on Netflix


SELECT COUNTRY,CNT
FROM
(SELECT UNNEST(STRING_TO_ARRAY(COUNTRY,','))AS COUNTRY,COUNT(*) AS CNT,DENSE_RANK() OVER(ORDER BY COUNT(*) DESC) AS DR
FROM NETFLIX_DATA
GROUP BY UNNEST(STRING_TO_ARRAY(COUNTRY,',')))T
WHERE T.DR<=5;


--5. Identify the longest movie

SELECT TITLE,DURATION
FROM NETFLIX_DATA
WHERE DURATION IN(
SELECT MAX(DURATION)
FROM NETFLIX_DATA
WHERE TYPEE IN('Movie'));


--6. Find content added in the last 5 years

SELECT TITLE
FROM NETFLIX_DATA
WHERE TO_DATE(DATE_ADDED,'MONTH DD,YYYY')>=CURRENT_DATE - INTERVAL '5 YEARS';


--7. Find all the movies/TV shows by director 'Rajiv Chilaka'!

SELECT TITLE
FROM NETFLIX_DATA
WHERE DIRECTOR LIKE '%Rajiv Chilaka%';


--8. List all TV shows with more than 5 seasons

SELECT TITLE
FROM NETFLIX_DATA
WHERE TYPEE IN('TV Show')
AND SPLIT_PART(DURATION,' ',1)::NUMERIC>5;

--9. Count the number of content items in each genre

SELECT UNNEST(STRING_TO_ARRAY(LISTED_IN,',')) AS GENERE,COUNT(*)
FROM NETFLIX_DATA
GROUP BY UNNEST(STRING_TO_ARRAY(LISTED_IN,','));

--10.Find each year and the average numbers of content release in India on netflix. 
--return top 5 year with highest avg content release!

WITH TOTAL_COUNT AS
(
SELECT COUNT(*) AS CNT
FROM NETFLIX_DATA
WHERE COUNTRY IN('India')
),
COUNT_YEAR AS
(
SELECT EXTRACT(YEAR FROM TO_DATE(DATE_ADDED,'MONTH DD,YYY')) AS RDATE,COUNT(*) AS CNT
FROM NETFLIX_DATA
WHERE COUNTRY ='India'
GROUP BY EXTRACT(YEAR FROM TO_DATE(DATE_ADDED,'MONTH DD,YYY'))
),
TOP_5 AS
(
SELECT COUNT_YEAR.RDATE AS YEARR,ROUND(COUNT_YEAR.CNT::NUMERIC/TOTAL_COUNT.CNT::NUMERIC*100 )AS AVG_RELEASE,DENSE_RANK() OVER(ORDER BY ROUND(COUNT_YEAR.CNT::NUMERIC/TOTAL_COUNT.CNT::NUMERIC*100 )) AS DR
FROM COUNT_YEAR CROSS JOIN TOTAL_COUNT
)

SELECT TOP_5.YEARR,TOP_5.AVG_RELEASE
FROM TOP_5
WHERE TOP_5.DR<=5;

--11. List all movies that are documentaries

SELECT TITLE
