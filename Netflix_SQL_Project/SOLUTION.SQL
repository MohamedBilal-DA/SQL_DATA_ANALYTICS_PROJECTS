--13. Find how many movies actor 'Salman Khan' appeared in last 10 years!

SELECT COUNT(*)
FROM NETFLIX_DATA
WHERE CASTT LIKE '%Salman Khan%'
AND RELEASING_YEAR>=EXTRACT(YEAR FROM CURRENT_DATE)-10::NUMERIC;


--14. Find the top 10 actors who have appeared in the highest number of movies produced in India.

SELECT ACTOR
FROM(
SELECT UNNEST(STRING_TO_ARRAY(CASTT,',')) AS ACTOR,COUNT(*),ROW_NUMBER() OVER(ORDER BY COUNT(*) DESC) RN
FROM NETFLIX_DATA
WHERE COUNTRY ILIKE '%India%'
GROUP BY UNNEST(STRING_TO_ARRAY(CASTT,',')))T
WHERE T.RN<=10;


15.
Categorize the content based on the presence of the keywords 'kill' and 'violence' in 
the description field. Label content containing these keywords as 'Bad' and all other 
content as 'Good'. Count how many items fall into each category.

CREATE OR REPLACE VIEW ADVISORY AS
(
SELECT TITLE,
CASE WHEN DESCRIPTION ILIKE '%kill%' OR
          DESCRIPTION ILIKE '%violence%'
		  THEN 'BAD_FLIM'
	 ELSE 'GOOD_FLIM'
END CATEGORY
FROM NETFLIX_DATA);

SELECT CATEGORY,COUNT(*)
FROM ADVISORY
GROUP BY CATEGORY;



